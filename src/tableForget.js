export function fetchDistances() {

    let distances = [];
    for (var distance in tableForget)
        distances.push(distance);

    return distances
}

export function fetchBestTimesByCategory(distance) {
    const distanceData = tableForget[distance];
    return distanceData.map(data => {
        return { 'category': data.category, 'time': data.timeByPercentages['100%'] }
    });
}


export function fetchCategoryMetadata(distance, bestTime) {
    return tableForget[distance].find(item => item.timeByPercentages['100%'] === bestTime)
}

export function generateDataTable(categoryName) {

    let categoryByDistance = [];
    for (let distance in tableForget) {
        let distanceCategory = tableForget[distance].find(item => item.category === categoryName);
        distanceCategory = { ...distanceCategory, distance }
        categoryByDistance.push(distanceCategory);
    }


    return percentages.map(percentage => {
        let obj = { percentage }

        categoryByDistance.forEach(category => {
            let { timeByPercentages, distance } = category
            if (timeByPercentages)
                obj[distance] = timeByPercentages[percentage]
        })

        return obj
    })

}
